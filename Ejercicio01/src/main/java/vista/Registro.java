    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed

    try {     
        Alumno1 alumno = new Alumno1();
        alumno.setNombre(txtnombre.getText());
        alumno.setCodigo(txtcodigo.getText());
        alumno.setDireccion(txtdireccion.getText());
        alumno.setEdad(Integer.parseInt(txtedad.getText()));
        
        Date fechaSeleccionada = JDC_Fecha.getDate();
    if (fechaSeleccionada != null) {
        // Formatear la fecha a un String con el formato deseado (dd/MM/yyyy)
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        String fechaFormateada = formatoFecha.format(fechaSeleccionada);
        
        // Convertir el String 'fechaFormateada' de nuevo a un objeto Date
        Date fechaConvertida = formatoFecha.parse(fechaFormateada);
        
        // Asignar la fecha convertida como Date al alumno
        alumno.setFecha(fechaConvertida);  // AquÃ­ se usa el Date convertido
        
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, selecciona una fecha vÃ¡lida");
        return;  // Salir del try si no hay fecha vÃ¡lida
    }
        listaAlumno.add(alumno);
        refrescarTabla();
      } catch (NumberFormatException e) {
          JOptionPane.showMessageDialog(this, "ERROR AL AGREGAR ALUMNO");
        } catch (ParseException ex) {
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
        }
      limpiar();
    }//GEN-LAST:event_btn_agregarActionPerformed

